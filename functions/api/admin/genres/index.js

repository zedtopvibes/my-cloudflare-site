export async function onRequest(context) {
    const { request, env } = context;
    
    const headers = {
        'Access-Control-Allow-Origin': '*',
        'Access-Control-Allow-Methods': 'GET, POST, PUT, DELETE, OPTIONS',
        'Access-Control-Allow-Headers': 'Content-Type',
        'Content-Type': 'application/json'
    };
    
    if (request.method === 'OPTIONS') {
        return new Response(null, { headers });
    }
    
    // GET - List all genres with stats
    if (request.method === 'GET') {
        try {
            // First, get all genres from the genres table
            const { results: genres } = await env.DB.prepare(`
                SELECT id, name, slug, description, cover_url, is_featured, created_at, updated_at
                FROM genres
                WHERE deleted_at IS NULL
                ORDER BY is_featured DESC, name ASC
            `).all();
            
            // For each genre, get counts from content tables
            const genresWithStats = await Promise.all(genres.map(async (genre) => {
                // Count artists with this genre
                const artistsCount = await env.DB.prepare(`
                    SELECT COUNT(*) as count FROM artists 
                    WHERE deleted_at IS NULL AND status = 'published' AND genre = ?
                `).bind(genre.name).first();
                
                // Count albums with this genre
                const albumsCount = await env.DB.prepare(`
                    SELECT COUNT(*) as count FROM albums 
                    WHERE deleted_at IS NULL AND status = 'published' AND genre = ?
                `).bind(genre.name).first();
                
                // Count EPs with this genre
                const epsCount = await env.DB.prepare(`
                    SELECT COUNT(*) as count FROM eps 
                    WHERE deleted_at IS NULL AND status = 'published' AND genre = ?
                `).bind(genre.name).first();
                
                return {
                    ...genre,
                    stats: {
                        artists: artistsCount?.count || 0,
                        albums: albumsCount?.count || 0,
                        eps: epsCount?.count || 0,
                        total: (artistsCount?.count || 0) + (albumsCount?.count || 0) + (epsCount?.count || 0)
                    }
                };
            }));
            
            return new Response(JSON.stringify(genresWithStats), { headers });
            
        } catch (error) {
            console.error('Error fetching admin genres:', error);
            return new Response(JSON.stringify({ error: error.message }), { 
                status: 500, 
                headers 
            });
        }
    }
    
    // POST - Create new genre
    if (request.method === 'POST') {
        try {
            const { name, description, cover_url, is_featured } = await request.json();
            
            if (!name) {
                return new Response(JSON.stringify({ error: 'Genre name is required' }), { 
                    status: 400, 
                    headers 
                });
            }
            
            // Generate slug
            const slug = name
                .toLowerCase()
                .replace(/\s+/g, '-')
                .replace(/[^\w-]/g, '')
                .replace(/--+/g, '-')
                .replace(/^-+|-+$/g, '');
            
            // Check if genre already exists
            const existing = await env.DB.prepare(`
                SELECT id FROM genres WHERE name = ? OR slug = ?
            `).bind(name, slug).first();
            
            if (existing) {
                return new Response(JSON.stringify({ error: 'Genre already exists' }), { 
                    status: 400, 
                    headers 
                });
            }
            
            const result = await env.DB.prepare(`
                INSERT INTO genres (name, slug, description, cover_url, is_featured, created_at, updated_at)
                VALUES (?, ?, ?, ?, ?, CURRENT_TIMESTAMP, CURRENT_TIMESTAMP)
                RETURNING id
            `).bind(
                name,
                slug,
                description || null,
                cover_url || null,
                is_featured ? 1 : 0
            ).run();
            
            const newGenre = await env.DB.prepare(`
                SELECT id, name, slug, description, cover_url, is_featured, created_at, updated_at
                FROM genres WHERE id = ?
            `).bind(result.results[0].id).first();
            
            return new Response(JSON.stringify(newGenre), { 
                status: 201, 
                headers 
            });
            
        } catch (error) {
            console.error('Error creating genre:', error);
            return new Response(JSON.stringify({ error: error.message }), { 
                status: 500, 
                headers 
            });
        }
    }
    
    return new Response(JSON.stringify({ error: 'Method not allowed' }), { 
        status: 405, 
        headers 
    });
}