export async function onRequest(context) {
    const { request, env } = context;
    
    const headers = {
        'Access-Control-Allow-Origin': '*',
        'Access-Control-Allow-Methods': 'GET, OPTIONS',
        'Access-Control-Allow-Headers': 'Content-Type',
        'Content-Type': 'application/json'
    };
    
    if (request.method === 'OPTIONS') {
        return new Response(null, { headers });
    }
    
    if (request.method !== 'GET') {
        return new Response(JSON.stringify({ error: 'Method not allowed' }), { 
            status: 405, 
            headers 
        });
    }
    
    try {
        // Get genres from artists (all, including drafts for admin)
        const artistGenres = await env.DB.prepare(`
            SELECT genre, COUNT(*) as count FROM artists 
            WHERE deleted_at IS NULL AND genre IS NOT NULL AND genre != ''
            GROUP BY genre
        `).all();
        
        // Get genres from albums (all, including drafts)
        const albumGenres = await env.DB.prepare(`
            SELECT genre, COUNT(*) as count FROM albums 
            WHERE deleted_at IS NULL AND genre IS NOT NULL AND genre != ''
            GROUP BY genre
        `).all();
        
        // Get genres from eps (all, including drafts)
        const epGenres = await env.DB.prepare(`
            SELECT genre, COUNT(*) as count FROM eps 
            WHERE deleted_at IS NULL AND genre IS NOT NULL AND genre != ''
            GROUP BY genre
        `).all();
        
        // Combine all genres
        const allGenres = new Map();
        
        artistGenres.results.forEach(g => {
            if (g.genre) {
                allGenres.set(g.genre, { 
                    name: g.genre, 
                    artists: g.count, 
                    albums: 0, 
                    eps: 0 
                });
            }
        });
        
        albumGenres.results.forEach(g => {
            if (g.genre) {
                const existing = allGenres.get(g.genre) || { name: g.genre, artists: 0, albums: 0, eps: 0 };
                existing.albums = g.count;
                allGenres.set(g.genre, existing);
            }
        });
        
        epGenres.results.forEach(g => {
            if (g.genre) {
                const existing = allGenres.get(g.genre) || { name: g.genre, artists: 0, albums: 0, eps: 0 };
                existing.eps = g.count;
                allGenres.set(g.genre, existing);
            }
        });
        
        const results = Array.from(allGenres.values()).map(genre => ({
            name: genre.name,
            slug: genre.name.toLowerCase().replace(/\s+/g, '-').replace(/[^\w-]/g, ''),
            stats: {
                artists: genre.artists || 0,
                albums: genre.albums || 0,
                eps: genre.eps || 0,
                total: (genre.artists || 0) + (genre.albums || 0) + (genre.eps || 0)
            }
        })).sort((a, b) => b.stats.total - a.stats.total);
        
        return new Response(JSON.stringify(results), { headers });
        
    } catch (error) {
        console.error('Error fetching admin genres:', error);
        return new Response(JSON.stringify({ error: error.message }), { 
            status: 500, 
            headers 
        });
    }
}